import Foundation

//1. 두 수의 합

func solution(_ num1:Int, _ num2:Int) -> Int {
    return num1 + num2
}

//2. 두 수의 차

func solution(_ num1:Int, _ num2:Int) -> Int {
    return num1 - num2
}

//3. 두 수의 곱

func solution(_ num1:Int, _ num2:Int) -> Int {
    return num1 * num2
}

//4. 몫 구하기

func solution(_ num1:Int, _ num2:Int) -> Int {
    return num1 / num2
}
